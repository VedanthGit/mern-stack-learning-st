// Q1. Print full name
// Write a function called `printFullName` that takes two parameters — `firstName` and `lastName` — and prints the full name joined by a space.
// Call the function twice with different names.

function printFullName(firstName, lastName) {
	console.log(firstName + " " + lastName);
}

printFullName("Vijaya", "Dairy");
printFullName("John", "Doe");
